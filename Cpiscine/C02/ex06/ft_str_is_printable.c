#include <unistd.h>

int	ft_str_is_printable(char *str)
{
	unsigned int	i;

	if (str[0] == '\0')
		return (1);
	else
	{
		i = 0;
		while (str[i] != '\0')
		{
			if (str[i] < 32 || str[i] > 126)
			{
				return (0);
			}
			i++;
		}
		return (1);
	}
}
